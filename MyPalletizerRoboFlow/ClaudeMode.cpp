void ClaudeMode::drawUsageBars() {
    int s = usage_data.session_pct;
    int w = usage_data.weekly_pct;
    int bar_x = PANEL_X + PANEL_PAD;
    int bar_w = PANEL_W - 2 * PANEL_PAD;

    // Session panel
    M5.Lcd.setTextSize(3);
    M5.Lcd.setTextColor(COL_TEXT, COL_PANEL);
    M5.Lcd.setCursor(PANEL_X + PANEL_PAD, PANEL1_Y + PANEL_PAD);
    M5.Lcd.printf("%d%%  ", s);

    int bar1_y = PANEL1_Y + 38;
    drawBar(bar_x, bar1_y, bar_w, BAR_H, s, pct_color(s));
    drawResetTime(bar_x, bar1_y + BAR_H + 5, usage_data.session_reset_min);

    // Weekly panel
    M5.Lcd.setTextSize(3);
    M5.Lcd.setTextColor(COL_TEXT, COL_PANEL);
    M5.Lcd.setCursor(PANEL_X + PANEL_PAD, PANEL2_Y + PANEL_PAD);
    M5.Lcd.printf("%d%%  ", w);

    int bar2_y = PANEL2_Y + 38;
    drawBar(bar_x, bar2_y, bar_w, BAR_H, w, pct_color(w));
    drawResetTime(bar_x, bar2_y + BAR_H + 5, usage_data.weekly_reset_min);
}

void ClaudeMode::drawStatusLine() {
    static bool last_ok = false;
    static bool last_connected = false;
    static uint32_t last_draw = 0;

    bool changed = (usage_data.ok != last_ok) || (ble_connected != last_connected);
    if (!changed && millis() - last_draw < 1000) return;
    last_ok = usage_data.ok;
    last_connected = ble_connected;
    last_draw = millis();

    M5.Lcd.fillRect(0, 205, 270, 16, COL_BG);
    M5.Lcd.setTextSize(1);
    M5.Lcd.setCursor(MARGIN, 208);

    if (usage_data.ok) {
        M5.Lcd.setTextColor(COL_GREEN, COL_BG);
        M5.Lcd.printf("Connected [%s]", usage_data.status);
    } else if (ble_connected) {
        M5.Lcd.setTextColor(COL_GREEN, COL_BG);
        M5.Lcd.print("Connected, awaiting data...");
    } else {
        M5.Lcd.setTextColor(COL_AMBER, COL_BG);
        M5.Lcd.print("Waiting for connection...");
    }
}

void ClaudeMode::handleCommunication(MyPalletizerBasic &myCobot) {
    if (Serial.available() > 0) {
        while (Serial.available()) Serial2.write(Serial.read());
    }
    if (Serial2.available() > 0) {
        while (Serial2.available()) Serial.write(Serial2.read());
    }
}
